					河南省人力资源和社会保障厅<br>
关于发布2017年专业技术人员继续教育公需科目指南的通知<br>
 豫人社办函﹝2017﹞16号<br>

各省辖市、省直管县（市）人力资源社会保障局，省直有关单位，省属高校，省级专业技术人员继续教育基地：<br>
    为深入实施专业技术人才知识更新工程，提升专业技术人员创新创业能力和综合素质，根据《河南省专业技术人员继续教育条例》，现发布2017年河南省专业技术人员继续教育公需科目学习指南。
    一、学习内容<br>
按照围绕中心、服务大局、学以致用的原则，2017年河南省专业技术人员继续教育公需科目学习内容，以人力资源社会保障部会同有关单位组织编写的全国专业技术人才知识更新工程公需科目教材为基本参考，突出党的最新政策理论、省委省政府经济社会发展战略部署等内容（详见附件）。
二、学习任务和形式<br>
2017年河南省专业技术人员继续教育公需科目学习时间为每人每年不少于24学时。各地、各部门在公需科目指南内选择不少于18个学时的学习内容，其余学时可结合各地、各行业特点自主确定。专业技术人员可通过参加人力资源社会保障部门或经该部门备案的施教机构举办的面授或远程教育培训班完成学习任务。
 三、有关要求<br>
（一）保证教学质量。要严格把好公需科目远程课件质量关，课件须经省人力资源社会保障厅备案方可上线，督促远程施教机构保持网络顺畅安全。施教机构要严格按照申报内容和师资组织实施公需科目面授培训，保持良好教学环境。<br>
（二）强化监督指导。各级人力资源社会保障部门、行业主管部门要结合本地、本行业人才培养规划，加强沟通，落实责任，指导各类施教机构开展多种有效形式的教学活动，并健全教学质量评价考核体系，加强日常管理监督，积极推动本地、本系统公需科目学习任务的落实。<br>
（三）提升服务水平。各级人力资源社会保障部门要依托“河南省专业技术人员继续教育信息管理系统”（网址：www.hnzjgl.gov.cn），及时、严格认定公需科目学时，督促用人单位为专业技术人员参加继续教育提供必要的经费和时间保障，及时审核学时。要推动专业技术人员在继续教育信息管理系统注册工作，规范上传学习记录和申报学时。要鼓励各地、各行业和用人单位为专业技术人员提供公益性培训活动。<br>
附件：2017年河南省专业技术人员继续教育公需科目目录
